<?php

function addizione($a, $b){
    return $a + $b;    
}

function sottrazione($a, $b){
    return $a - $b;    
}

function prodotto($a, $b){
    return $a * $b;    
}

function quoziente($a, $b){
    return $a / $b;    
}


$a = 12;
$b = 4;

echo addizione($a, $b);
echo sottrazione($a, $b);
echo prodotto($a, $b);
echo quoziente($a, $b);













